$(function(){
  $('.tab-btn li a').click(function(e){
    e.preventDefault();
    $('.tab-btn li a').removeClass('active'); 
    $(this).addClass('active'); 

    var key=$(this).attr('href').substring(1);
    console.log(key);
    
    $('.exhibition_list ul').empty();

    if(project[key].contents.length==0){
      alert('현재 전시 준비중입니다.');   
    }

    //내용변경하기 
    project[key].contents.forEach((obj, i) => {                               
      var position="left";
      if(i%2==0){
        position="right";
      }
      
      $('.exhibition_list ul').append(`
        <li class="content ${position}">
            <div class="img-area">
                <a href="#">
                    <img src="${obj.small[0]}" alt="${obj.description[1]}" class="big">
                </a>    
                <div class="btn-area">                                  
                    <span>${obj.category}</span>
                    <button><i class="icon ${obj.like}"></i></button>
                </div>
            </div>
            <div class="textbox">
                <b>${obj.description[0]}</b>
                <h3>${obj.description[1]}</h3>
                <dl>
                    <dt>${obj.description[2]}</dt>
                    <dd>${obj.description[3]}</dd>
                </dl>                                 
                <div class="small"></div>  
            </div>
        </li> 
      `)
      if(obj.small.length>1){
        obj.small.forEach(src => {
          console.log(i, src);        
          $('.exhibition_list ul li').eq(i).find('.small').append(`
              <a href="#"><img src="${src}" alt="${obj.description[1]}"></a>
          `)                
        });
      }
    }) 
  })

  $('.tab-btn li:nth-child(1) a').trigger('click');
  
})